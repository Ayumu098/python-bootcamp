import math

# Constants
print("Math Constants:")
print(f"Pi: {math.pi}")
print(f"Euler's Number: {math.e}")
print()

# Basic operations
print("Basic Operations:")
print(f"Square root of 16: {math.sqrt(16)}")
print(f"Factorial of 5: {math.factorial(5)}")
print(f"GCD of 28 and 35: {math.gcd(28, 35)}")
print()

# Trigonometry
print("Trigonometry:")
angle = math.radians(45)  # Convert degrees to radians
print(f"sin(45°): {math.sin(angle)}")
print(f"cos(45°): {math.cos(angle)}")
print(f"tan(45°): {math.tan(angle)}")
